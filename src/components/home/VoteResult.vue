    methods: {
        mouseMove() {
            // 擷取頁面位置 用於mousemove顯示地圖上資料
            let top = event.offsetY
            let left = event.offsetX
            let max_width = 200
            let left_contrast = left - 200

            // 定義票數
            let yes = Number(event.target.attributes[2].nodeValue)
            let no = Number(event.target.attributes[3].nodeValue)
            let total = yes + no

            // 票數對抗樣式
            let width_y = Math.ceil((yes / total) * 100)
            let width_n = 100 - width_y

            // 綁定縣市名稱
            document.querySelector('.hoverText>.city').textContent = event.target.attributes[1].nodeValue

            // 綁定同意票(number toString)
            document.querySelector('.hoverText .vote_yes').textContent = event.target.attributes[2].nodeValue
                .toString()
                .replace(/\B(?=(\d{3})+(?!\d))/g, ',')

            // 綁定不同意票(number toString)
            document.querySelector('.hoverText .vote_no').textContent = event.target.attributes[3].nodeValue
                .toString()
                .replace(/\B(?=(\d{3})+(?!\d))/g, ',')

            // mousemove時顯示資料的樣式

            if (left >= max_width) {
                // 寬度超過之後不能超過畫面
                document.querySelector('.hoverText').style.cssText = `
                display: block;
                top: ${top}px;
                left: ${left_contrast}px;
            `
            } else {
                document.querySelector('.hoverText').style.cssText = `
                display: block;
                top: ${top}px;
                left: ${left}px;
            `
            }

            document.querySelector('.bar_yes').innerHTML = width_y + '%'
            document.querySelector('.bar_no').innerHTML = width_n + '%'

            document.querySelector('.bar_yes').style.cssText = `
                width: ${width_y}%;
            `
            document.querySelector('.bar_no').style.cssText = `
                width: ${width_n}%;

            `
        },
        mouseLeave() {
            document.querySelector('.hoverText').style.display = 'none'
        },

        vote(state) {
            setTimeout(() => {
                // 票數format
                let vote_agree = document.querySelectorAll('.result_agree_vote')
                let vote_disagree = document.querySelectorAll('.result_disagree_vote')

                // 票數linechart
                let bar_agree = document.querySelectorAll('.result_agree_bar')
                let bar_disagree = document.querySelectorAll('.result_disagree_bar')

                for (let i = 0; i < state.votes.length; i++) {
                    // 票數判定:同意票
                    if (vote_agree[i].innerText < 7000000) {
                        bar_agree[i].style.width = Math.floor((vote_agree[i].innerText / state.vote_total) * 100) + '%'
                    } else {
                        bar_agree[i].style.width = 100 + '%'
                    }

                    // 票數判定:不同意票

                    if (vote_disagree[i].innerText < 7000000) {
                        bar_disagree[i].style.width =
                            Math.floor((vote_disagree[i].innerText / state.vote_total) * 100) + '%'
                    } else {
                        bar_disagree[i].style.width = 100 + '%'
                    }

                    vote_agree[i].innerText =
                        vote_agree[i].innerText.toString().replace(/\B(?=(\d{4})+(?!\d))/g, '萬') + ' 票'
                    vote_disagree[i].innerText =
                        vote_disagree[i].innerText.toString().replace(/\B(?=(\d{4})+(?!\d))/g, '萬') + ' 票'
                }
            }, 1000)

            // eslint-disable-next-line no-undef
        },
        handler() {
            this.target = event.target.innerHTML
        },
    },

    computed: {
        voting() {
            return this.$store.state.votes
        },
    },

    mounted() {
        this.vote(this.$store.state)
    },
